#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*i;
	char	*j;

	i = s1;
	j = (char*)s2;
	while (*i)
		i++;
	while (n-- > 0 && *j)
		*(i++) = *(j++);
	*i = '\0';
	return (s1);
}
